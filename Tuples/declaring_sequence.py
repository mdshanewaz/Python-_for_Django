#Tuple is a Sequence data type that is immutable
tup1 = ()
print(tup1)
print(type(tup1),"\n")

tup2 = 12, 13, 14, 15
print(tup2)
print(type(tup2),"\n")

tup3 = ("Shah")
print(tup3)
print(type(tup3),"\n")

tup4 = "Newaz"
print(tup4)
print(type(tup4),"\n")

tup5 = ("Shah",)
print(tup5)
print(type(tup5),"\n")

tup6 = (12, )
print(tup6)
print(type(tup6))

